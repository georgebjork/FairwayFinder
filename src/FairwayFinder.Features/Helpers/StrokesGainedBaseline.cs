/// <summary>
/// Static lookup table for expected strokes to hole out from a given distance and lie.
/// Data based on Mark Broadie's research (Every Shot Counts, 2014).
/// Scratch baseline loaded from CSV reference data; Bogey/HighHandicap currently fall back to Scratch.
/// </summary>
public static class StrokesGainedBaseline
{
    /// <summary>
    /// Returns expected strokes to hole out from a given distance and lie.
    /// </summary>
    public static double GetExpectedStrokes(int distance, string unit, LieType lie, BaselineLevel level = BaselineLevel.Scratch)
    {
        if (distance <= 0) return 0.0;

        if (lie == LieType.Green || unit == DistanceUnit.Feet)
        {
            return LookupGreen(distance);
        }

        return lie switch
        {
            LieType.Tee => LookupTee(distance),
            LieType.Fairway => LookupFairway(distance),
            LieType.Rough => LookupRough(distance),
            LieType.Sand => LookupSand(distance),
            LieType.Recovery => LookupRough(distance), // No Recovery-specific data; use Rough
            _ => LookupFairway(distance)
        };
    }

    /// <summary>
    /// Returns 0.0 — used when the ball is holed (EndDistance is null).
    /// </summary>
    public static double GetExpectedStrokesHoled() => 0.0;

    // ================================================================
    // O(1) array-based lookups — data from CSV reference tables
    // ================================================================

    // Green: feet 1..90 → GreenData[feet - 1]
    private static double LookupGreen(int feet)
    {
        if (feet < 1) return GreenData[0];
        if (feet > GreenMaxFeet) return GreenData[^1];
        return GreenData[feet - 1];
    }

    // Tee: yards 100..600 → TeeData[yards - 100]
    private static double LookupTee(int yards)
    {
        if (yards < TeeMinYards) return TeeData[0];
        if (yards > TeeMaxYards) return TeeData[^1];
        return TeeData[yards - TeeMinYards];
    }

    // Fairway/Rough/Sand: yards 3..600 → Data[yards - 3]
    private static double LookupFairway(int yards)
    {
        if (yards < ShotMinYards) return FairwayData[0];
        if (yards > ShotMaxYards) return FairwayData[^1];
        return FairwayData[yards - ShotMinYards];
    }

    private static double LookupRough(int yards)
    {
        if (yards < ShotMinYards) return RoughData[0];
        if (yards > ShotMaxYards) return RoughData[^1];
        return RoughData[yards - ShotMinYards];
    }

    private static double LookupSand(int yards)
    {
        if (yards < ShotMinYards) return SandData[0];
        if (yards > ShotMaxYards) return SandData[^1];
        return SandData[yards - ShotMinYards];
    }

